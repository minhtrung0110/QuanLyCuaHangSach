    private void search() throws Exception
    {
    	   
    	    while(tempncc.getRowCount()>0)
    	    {
    	    	tempncc.removeRow(0);
    	    }
            Vector header = new Vector();
	        header.add("Mã NCC");
	        header.add("Tên NCC");
	        header.add("Địa chỉ");
	        

            if (tempncc.getRowCount()==0)
                        { tempncc=new DefaultTableModel(header,0);} 


            if(cbTimKiemNCC.getSelectedItem() == "Mã NCC" )
            {
                    NCCDTO ncc = BNCC.timkiem_MaNCC(txTimKiem.getText());			    			
                    tempncc.addRow(addNCC(ncc));

            }		 

            if(cbTimKiemNCC.getSelectedItem() == "Tên NCC" )
            {
                    for(NCCDTO ncc : BNCC.timkiem_TenNCC(txTimKiem.getText()))
                    {
                            tempncc.addRow(addNCC(ncc));

                    }
            }
            if(cbTimKiemNCC.getSelectedItem() == "Địa chỉ" )
            {
                    for(NCCDTO ncc : BNCC.timkiem_DiaChi(txTimKiem.getText()))
                    {
                            tempncc.addRow(addNCC(ncc));

                    }
            }	
            if(tbNCC.getModel() == modelncc)
            tbNCC.removeAll();
            tbNCC.setModel(tempncc);   	
    }      
    
