    public void XoaNCC() throws Exception{
	if(tbNCC.getModel() == modelncc)
            {
              int x = tbNCC.getSelectedRow();
              if (x>=0)
		{
		 NCCDTO ncc =new NCCDTO();
		 ncc = BNCC.list().get(x);
		 
		 
		 BNCC.deleteNCC(ncc.getMaNCC());
                
		 modelncc.removeRow(x);
		 tbNCC.setModel(modelncc);
                
		 }
             }
        else if(tbNCC.getModel() == tempncc)
        {
            int x = tbNCC.getSelectedRow();
              if (x>=0)
		{
		 
		
                 BNCC.deleteNCC(tbNCC.getValueAt(x, 0).toString());
                 tempncc.removeRow(x);
                 initNCC();
                 tbNCC.setModel(tempncc);
                }
        }
}
