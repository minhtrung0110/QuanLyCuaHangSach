      private void docDSTG(){
        try{
            Vector header = new Vector();
            header.add("Mã"); header.add("Tên"); header.add("Địa Chỉ"); header.add("SĐT"); 
            DefaultTableModel model = new DefaultTableModel(header, 0);
            TacGiaBUS bus = new TacGiaBUS();
            if(bus.getListTacGia() == null)
                bus.loadDSTacGia();
            for(TacGiaDTO TG : bus.getListTacGia()){
                Vector row = new Vector();
                row.add(TG.getMaTG());
                row.add(TG.getTenTG());
                row.add(TG.getDiaChi());
                row.add(TG.getSDT());
                model.addRow(row);
            }
            table.setModel(model);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Không thể đọc dữ liệu, vui lòng thử lại");
        }
    }
