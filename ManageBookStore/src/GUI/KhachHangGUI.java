    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try{
        String ma = textMa.getText();
        String ho = textHo.getText();
        String ten = textTen.getText();
        String sdt = textSdt.getText();
        String email = textEmail.getText();
        int tct = Integer.parseInt(textTct.getText());
        
        Date date = dateChoser.getDate();
        
        int ngay = date.getDate();
        int thang = date.getMonth() + 1;
        int nam = date.getYear() + 1900;
        String ngaysinh = nam + "-" + thang + "-" + ngay;
        
        KhachHangDTO kh = new KhachHangDTO(ma, ho, ten, sdt, email, radioNam.isSelected(), tct);
        kh.setNgaySinh(ngaysinh);
        
        
        bus.them(kh);
        docDSKH();
        textAmount.setText(String.valueOf(model.getRowCount()));
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập lại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int index = table.getSelectedRow();
        int i = 0;
        textMa.setText(model.getValueAt(index, i++).toString());
        textHo.setText(model.getValueAt(index, i++).toString());
        textTen.setText(model.getValueAt(index, i++).toString());
        textSdt.setText(model.getValueAt(index, i++).toString());
        textEmail.setText(model.getValueAt(index, i++).toString());
        if(model.getValueAt(index, i++).toString().equals("nam"))
            radioNam.setSelected(true);
        else radioNu.setSelected(true);
        textTct.setText(model.getValueAt(index, i++).toString());
        String[] date = model.getValueAt(index, i++).toString().split("-", 3);
        Date selected = new Date(Integer.parseInt(date[0]) - 1900, Integer.parseInt(date[1]) - 1, Integer.parseInt(date[2]));
        dateChoser.setDate(selected);
    }//GEN-LAST:event_tableMouseClicked

